local Heroes = {};

-- 返回对应的 key value 属性表
Heroes.GetData = function(nID, nPlayers)
	local retTbl = nil;
	local prop = Property[nID];
	if prop then
		retTbl = {};
		retTbl.strName = prop.name or '';
		retTbl.nSpeed = prop.speed or -1;
		retTbl.nHP = prop.wounds or -1;
		retTbl.nArmor = prop.armor or -1;
		retTbl.nFatigue = prop.fatigue or -1;
		
		retTbl.nConquestValue = prop.conquest_value or -1;
		retTbl.nFightingSkills = prop.fighting_skills or -1;
		retTbl.nSubterfugeSkills = prop.subterfuge_skills or -1;
		retTbl.nWizardrySkills = prop.wizardry_skills or -1;
		retTbl.nMagicTrait = prop.magic_trait or -1;
		retTbl.nMeleeTrait = prop.melee_trait or -1;
		retTbl.nRangeTrait = prop.range_trait or -1;

		retTbl.strSkills = prop.abilities or ''; -- string.gsub(prop.abilities, '(%w+ -%w+)', TRANSLATE_TABLE);
		retTbl.strExpandsion = string.gsub(prop.expansion, '(%w+)', TRANSLATE_TABLE) or prop.expansion or '';
	end
	
	return retTbl;
end

return Heroes;