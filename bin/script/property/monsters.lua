local Monsters = {};

-- 返回对应的 key value 属性表
Monsters.GetData = function(nID, nPlayers)
	local retTbl = nil;
	local prop = Property[nID];
	if prop then
		retTbl = {};
		retTbl.strName = prop.name or '';
		retTbl.bMaster = prop.master == "yes";
		retTbl.nSpeed = prop.speed or -1;
		-- 根据人数设定对应的血量
		local nIdx = 2;
		for hp in string.gmatch(prop.wounds, "%d+") do
			if nIdx == nPlayers then
				retTbl.nHP = hp or -1;
				break;
			end
			nIdx = nIdx + 1;
		end
	
		retTbl.nArmor = prop.armor or -1;
		retTbl.strAttackType = string.gsub(prop.attack, '(%w+)', TRANSLATE_TABLE);
		retTbl.strAttackDice = string.gsub(prop.attack_dice, '(%w+)', TRANSLATE_TABLE);
		retTbl.strSkills = prop.abilities; -- string.gsub(prop.abilities, '(%w+ -%w+)', TRANSLATE_TABLE);
		retTbl.strExpandsion = string.gsub(prop.expansion, '(%w+)', TRANSLATE_TABLE);
	end
	
	return retTbl;
end

return Monsters;
